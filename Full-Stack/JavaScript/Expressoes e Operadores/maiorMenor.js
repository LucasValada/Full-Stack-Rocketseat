let balance = 500;
let payment = 120;

// > Maior que
console.log(balance > payment);

// < Menor que
console.log(balance < payment);

// >= Maior ou igual
console.log(balance >= payment);

// <= Menor ou igual
console.log(balance <= payment);

// == Igual a
console.log(balance == payment);

// != Diferente de
console.log(balance != payment);
