let value;

// operador atribuição

value = 2;
console.log(value);

//Incremento
value += 2;
console.log(value);

//Decremento

value -= 2;
console.log(value);

// Multiplicação
value *= 3;
console.log(value);

// Divisão
value /= 3;
console.log(value);

// Resto
value %= 3;
console.log(value);

// Potenciacao
value **= 3;
console.log(value);

// Exponenciacao
value **= 3;
console.log(value);
